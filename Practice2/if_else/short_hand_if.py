# Example 1: Simple ternary
age = 18
status = "Adult" if age >= 18 else "Child"
print(status)  # Output: Adult

# Example 2: Check number positivity
num = -5
result = "Positive" if num > 0 else "Non-positive"
print(result)  # Output: Non-positive

# Example 3: Boolean ternary
is_sunny = True
activity = "Go outside" if is_sunny else "Stay inside"
print(activity)  # Output: Go outside
