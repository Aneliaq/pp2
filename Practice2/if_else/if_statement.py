# Example 1: Simple if statement
x = 10
if x > 5:
    print("x is greater than 5")  # This line runs because condition is True

# Example 2: If with string comparison
color = "red"
if color == "red":
    print("Color is red")

# Example 3: If with boolean variable
is_hungry = True
if is_hungry:
    print("Eat something")
